package loops;

public class While_loop {

	public static void main(String[] args) {
	//	I WRITE PANKAJ 5 TIMES increment
	// DESAI Decrement 5 times
		
		int i=1;
		while(i<=5) {
			System.out.println("PANKAJ");
			i++;
		}
		int j=5;
		while(j>=1) {
			System.out.println("DESAI");
			j--;
		}
	}

}
